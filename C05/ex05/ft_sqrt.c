int		ft_sqrt(int nb)
{
	unsigned int i;
	unsigned int nb2;

	nb2 = nb;
	i = 1;
	if (nb < 1)
		return (0);
	while ((i * i) < nb2)
		i++;
	if ((i * i) == nb2)
		return (i);
	return (0);
}
