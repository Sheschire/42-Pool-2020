#include <unistd.h>

int		main(int argc, char **argv)
{
	if (argc != 4)
		write(1, "\n", 1);
	else
	{
		while (*argv[1])
		{
			if (*argv[2] == *argv[1])
				*argv[1] = *argv[3];
			write(1, argv[1], 1);
			argv[1]++;
		}
		write(1, "\n", 1);
	}
}
